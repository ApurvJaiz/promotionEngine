package com.engine.model;

public class Inventory 
{
	char name;
	float price;
	Inventory(){}
	public char getName() {
		return name;
	}
	public void setName(char name) {
		this.name = name;
	}
	public float getPrice() {
		return price;
	}
	public void setPrice(float price) {
		this.price = price;
	}
	

}
